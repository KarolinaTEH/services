In den letzten Wochen wurden weltweit enorme Anstrengungen unternommen, um die vierte Welle der COVID-19-Pandemie einzudämmen. 
Zum 2. Dezember 2021 waren wir zuversichtlich, dass die Veranstaltung im Februar 2022 im ursprünglichen, d.h. physischen Format, stattfinden würde. 

Als Veranstalter waren wir bereit, alle notwendigen Hygiene- und Sicherheitsstandards einzuhalten. 
Um die Sicherheit aller Teilnehmer zu gewährleisten, wurden entsprechende Maßnahmen getroffen. 
**Die neuen Beschränkungen der Bundesregierung zur Bekämpfung der COVID-19-Pandemie vom 02.12.2021 reduzieren aber drastisch die potentiellen Teilnehmerzahlen für Veranstaltungen dieser Art.**

Die mit der Organisation der Veranstaltung verbundenen Risiken wurden gründlich analysiert. 
Nach Rücksprache mit dem Berlin Convention Office und dem Venue-Betreiber haben wir die einzig mögliche Entscheidung getroffen - die E-commerce Berlin Expo auf den **5. Mai 2022** zu verschieben, um die geplante Formel erfolgreich umzusetzen. **Darüber hinaus werden die mit euren Buchungen verbundenen Leistungen und Verpflichtungen ohne zusätzliche Kosten auf den neuen Termin übertragen.**

Unsere Entscheidung wurde im Interesse unserer Kunden und der Maximierung des ROI der Veranstaltung getroffen. 
Aufgrund der aktuellen Beschränkungen und der Kapazitäten der STATION Berlin konnte die Veranstaltung in geschlossener Form für nur etwa 1000-1500 Gäste durchgeführt werden. Eine niedrige Zahl würde wahrscheinlich nicht die angemessene Kapitalrendite für die Veranstaltung ermöglichen, und wir sind uns bewusst, dass dieses Ergebnis für keinen unserer Kunden zufriedenstellend sein kann.

Wir wissen um den Wert von Face-to-Face-Meetings und sind uns bewusst, dass virtuelle oder hybride Meeting-Formate Live-Meetings nicht ersetzen können. 
Die E-Commerce Berlin Expo ist ein Ökosystem von Projekten. Nicht nur die Messe, aber auch die Konferenz, die E-commerce Germany Awards und der Treffpunkt für die Experten machen dieses Event aus. Wir sind überzeugt, dass wir uns bei dem neuen Veranstaltungstermin im Mai in Berlin treffen und das, was wir für euch vorbereitet worden ist, voll ausgeschöpft werden kann. 

Vielen Dank für euer Vertrauen und eure Unterstützung. 
Wir entschuldigen uns für diese außerhalb unseres Machtbereichs liegende Situation und bitten um euer Verständnis.

Bei Fragen wenden euch bitte an euren Account Manager.

