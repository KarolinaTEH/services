## Parkowanie (dzień przed eventem, dzień eventu)

Przy pierwszym wjeździe na teren EXPO XXI, bilet parkingowy należy pobrać z parkomatu. 
**Do bezpłatnego wyjazdu** potrzebna będzie karta parkingowa, który dostaniecie Państwo od nas przy pierwszym wspólnym spotkaniu z nami (dzień montażowy lub rano - przed wydarzeniem). **Do kolejnych wjazdów i wyjazdów trzeba korzystać z karty parkingowej (niczego nie pobieramy z parkomatu)**

## Dzień montażowy (opcjonalny) - 13 kwietnia

Godziny montażowe to 16:00 - 20:00 (o ile nie ustalono z nami inaczej). 

Po wejściu na Halę #1 (wejście po lewej patrząc od wejścia głównego) należy udać się do punktu odbioru welcome-packów (początek Hali #1), w którym będzie czekał Państwa Opiekun i zaprowadzi Państwa do właściwego stoiska.

## Dzień Targowy - 14 kwietnia

Montaż w dzień targowy możliwy jest tylko i wyłącznie w godzinach **8:00 - 9:30.** (O 10:00 hala zostanie otwarta do publicznego zwiedzania.)

Jeśli nie odbieraliście Państwo welcome-packu w opcjonalny dzień montażowy, w dzień targowy należy udać się do punktu informacyjnego dla Wystawców znajdującego się w strefie VIP (Hala #3 - po prawej stronie od wejścia głównego do kompleksu hal)

W razie wystąpienia jakichkolwiek problemów i wątpliwości związanych z Państwa stoiskiem (np z prądem) należy udać się do informacji dla wystawców (Hala #3 / Strefa VIP) lub wysłać SMS na numer do Opiekuna, który znajdzie się w pakiecie powitalnym.

**Demontaż** jest możliwy od 17:15. Hala będzie dostępna do 20:00 


