## Nasza poprzednia wiadomość


W związku z wprowadzeniem w całym kraju tzw. "źółtej strefy", która narzuca dodatkowe obostrzenia także względem Organizatorów imprez takich jak Targi - m.in. limit 1 osoby na każde 4m2 powierzchni Targowej z (wyłączeniem obsługi) zwracamy się do Ciebie z ankietą.

**Zanim ją wypełnisz prosimy Cię o poświęcenie chwili na zapoznanie się z naszą sytuacją.**

Kwietniowe Targi w Krakowie nie mogły się odbyć z uwagi na "lockdown" całego kraju i prawny zakaz organizacji tego typu wydarzeń. Pragnąc utrzymać jak najlepsze relacje z naszymi Wystawcami dokonaliśmy bezkosztowego przeniesienia rezerwacji na Warszawę. Takie rozwiązanie wydawało nam się wtedy najlepsze. 

Lepsze niż podpieranie się "siłą wyższą", regulaminem Targów czy nawet przepisami Kodeksu Cywilnego i anulowanie rezerwacji bez zwrotu płatności lub ze zwrotem uwzględniajacym uzasadnione koszty poniesione przez Organizatora (jeśli by patrzeć tylko na przepisy Kodeksu Cywilnego).

Aktualna sytuacja epidemiczna w Polsce i obowiązujący stan prawny umożliwiają wprawdzie realizację zaplanowanych Targów ale mocno ograniczą organizacyjny rozmach, do którego przyzwyczailiśmy naszych Wystawców, Prelegentów i Zwiedzajacych. Na halę będzie mogło wejść niewiele więcej niż 1000 Zwiedzających czyli od 3 do 4x mniej niż w poprzednich edycjach. 

Dochodzą do nas głosy, że oprócz obaw związanych ze zdrowiem, biorąc pod uwagę całościowe koszty związane z logistyką wystawiania się boicie się Państwo o o wiele gorszy "zwrot z inwestycji". 

Naszym zadaniem jest zweryfikować zasadność takich głosów konfrontując je z zdaniem wszystkich, których sytuacja dotyczy.

**Na ten moment rozważamy 2 opcje:**

A) realizację Targów zgodnie z planem i dostosowanie ich do świeżo ogłoszonych ograniczeń (odmówienie części zarejestrowanym uczestnikom wejścia)

B) zmianę terminu na II kwartał 2021 (lokalizacja Kraków bądź Warszawa). Wtedy 23/10 nagralibyśmy tylko prezentacje Prelegentów.

