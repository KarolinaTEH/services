## Event Day - 17th of April

In the standard offering (without buying additional one) you have a **one parking card**, which is only needed to leave the parking area during event day.

Additional setup hours are between **8:00am and 9:30pm.**

After entering the hall you need to go to the information point for the exhibitors. Your Account Manager (or team member in charge) should be there.

At 10:00am we will open gates and welcome visitors.

In case of any problem (e.g., with electricity) you should go to the information for exhibitors or text your Account Manager, whose number you will find in the welcome pack.

**Disassembly** is possible from 5:15pm till 8:00pm.


## Welcome pack

During our first meeting (optional setup day or immediately before the event) you will be handed a welcome pack with: 

* the badges of your representatives if you have added them before the deadline at your exhibitor's panel, 
* lunch vouchers, 
* contact numbers to the important staff of Expo, 
* parking card
* any additionally paid vouchers, parking cards...


## Additional recommendations

** Please take your own power extension cord / bar ** (of course we have some backup but...)

** Remember, that standard power access is only 300 Watts ** 

If you plan to use something more that, for instance, 2 laptops and TV you need to buy an extra package (3000 Watts).  If you want to consult power usage of your equipment, please contact with us.



