## Expotag: 13.02.2020 von 10-17 

Das Parken auf dem Gelände der Expo ist am Tag der Expo nicht möglich. 

Neben dem Expogelände gibt es **ein Parkhaus**, welches benutzt werden kann. **Das Tagesticket kostet 10€.**

Seid ihr in euren Hallen angekommen, begebt euch bitte zum Infopunkt. Es wird zwei geben (1x pro Halle). Dort wird euer Account Manager auf euch warten. Dort bekommt ihr euer Willkommensbag ausgehändigt, was folgendes enthält: 

* Ausstellerpässe der registrierten Vertreter 
* Catering Voucher 
* WLAN-Zugriff (Login-Daten) 
* Kontaktdaten des Expoteams vor Ort 

Wichtig! Jede Flächenkategorie innerhalb der 9m2 haben 500W Strom. Die Grandkategorie hat 1000W. 

Wenn du keinen zusätzlichen Strom gebucht  und du vor hast Hochleistungsgeräte, wie eine Kaffemaschine anzuschließen, dann ist das verboten.

Sollte es Probleme mit dem Strom oder anderem geben, gebt eurem Account Manager bescheid. Die Kontaktdaten werden im Willkommensbage sein. 

## Abbau: 13.02.2020 17:15-24:00 

