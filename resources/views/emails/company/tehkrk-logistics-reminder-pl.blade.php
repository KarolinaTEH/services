## Dzień Targowy - 17 kwietnia

Montaż w dzień targowy możliwy jest tylko i wyłącznie w godzinach **8:00 - 9:30.** (O 10:00 hala zostanie otwarta do publicznego zwiedzania.)

Po wejściu na halę należy udać się do punktu informacyjnego dla Wystawców na którym będzie czekał Państwa Opiekun, który zaprowadzi Państwa do właściwego stoiska.

O ile nie została dokupiona dodatkowa, standardowo mają Państwo do dyspozycji jedną kartę parkingową, która jest potrzebna **tylko do wyjazdu z parkingu.**

Karta parkingowa będzie znajdowała się w "welcome packu" na stoisku

**Demontaż** jest możliwy od 17:15. Hala będzie dostępna do 20:00.

W razie wystąpienia jakichkolwiek problemów i wątpliwości związanych z Państwa stoiskiem (np z prądem) należy udać się do informacji dla wystawców lub dzwonić na numer do Opiekuna, który znajduje się w kopercie / pakiecie powitalnym.


## Welcome pack

Welcome pack zostanie przekazany podczas naszego pierwszego spotkania (16 kwietnia lub w dzień targów). 

* identyfikatory przedstawicieli (jeśli zostali dodani w Panelu Wystawcy :), 
* vouchery obiadowe, 
* numery kontaktowe do osób z obsługi targów, 
* karta parkingowa.


