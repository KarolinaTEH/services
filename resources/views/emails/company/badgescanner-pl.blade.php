# Instalacja aplikacji do skanowania

## Zainstaluj aplikację EXPO, która jest potrzebna do uruchomienia naszej aplikacji skanującej

<img src="https://res.cloudinary.com/eventjuicer/image/upload/v1555447079/expoclient.png" style="max-height: 150px;" />

Jak to zrobić? 

Otwórz [Google Play](https://play.google.com/store/apps/details?id=host.exp.exponent&referrer=www) lub [App Store](https://itunes.apple.com/app/apple-store/id982107779?ct=www&mt=8) i wyszukaj aplikację "Expo". Zainstaluj.

## Otwórz aplikację Expo. Musisz wyrazić zgodę na wykorzystanie aparatu.

## Z obojętnie jakiego urządzenia podłączonego do Internetu wejdź na stronę [http://teh.com.pl/m](http://teh.com.pl/m)

* **Android** - otwórz ponownie / wróć do aplikacji Expo. Zeskanuj kod widoczny na stronie http://teh.com.pl/m lub znajdź sekcję "Request a link" i wpisz adres email, który możesz otworzyć na telefonie, który ma służyć do skanowania i ma zainstalowaną aplikację Expo

* **iOS** - otwórz aparat, zeskanuj kod kreskowy widoczny na wcześniej otwartej stronie http://teh.com.pl/m . Powinna uruchomić się nasza aplikacja do skanowania identyfikatorów

## Mając otwartą nasza aplikację do skanowania musisz się już tylko zalogować jako swoja firma. 

Wejdź z innego telefonu lub laptopa na stronę [https://targiehandlu.pl/](https://targiehandlu.pl/) aby wygenerować kod QR służący do logowania 

Aby uzyskać kod potrzebny do logowania kliknij na klucz w prawym górnym rogu, podaj adres e-mail, który był podany jako adres przedstawiciela na stoisku

<img src="https://res.cloudinary.com/eventjuicer/image/upload/v1555447873/scanner-unlock.png" style="max-width: 300px;" />

Jeśli kod nie generuje się - podany adres e-mail nie był podany jako adres przedstawiciela firmy na stoisku. Należy podać adres e-mail innej osoby ze stoiska.

Po zeskanowaniu kodu powinieneś zobaczyć nazwe swojej firmy.

